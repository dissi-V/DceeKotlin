fun main() {
    //Arithmeticoperators
    var num1 = 45
    var num2 = 34
    println(num1 + num2)
    println(num1 - num2)
    println(num1 / num2)
    println(num1 * num2)
    println(num1 + num2)

    //Comparison operators
    println(num1 > num2)
    println(num1 < num2)
    println(num1 >= num2)
    println(num1 >= num2)
    println(num1 == num2) //Equal to
    println(num1 != num2) //not equal to

    //Logic operators - and, or and not
    println(num1 < num2 && num1 !=num2)
    println(num1 < num2 || num1 !=num2)
    println(!(num1 < num2 || num1 !=num2))
}